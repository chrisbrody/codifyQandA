    <!-- Step By Step for Class Time -->
    <div class="classTime">
      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week10Step1">Chit chat - quick intro - talk about what was the coolest / best thing learned this week with JS</li>
        <li id="Week10Step2">
          Talk about JS - how to include - where and why
          <ul>
            <li>start with how to include <code>&lt;script src="js.main.cs"&gt;&lt;/script&gt;</code> tag and mention you no longer need to include the <code>type="type/javascript"</code> - since browsers recognise script tags as JavaScript</li>
            <li>show where to to include - top &amp; bottom and which is better - the latter is better due to load times</li>
          </ul>
        </li>
        <li id="Week10Step3">
          Discuss how &amp; when to use console.log
          <ul>            
            <li>show how to access the console using inspect element</li>
            <li>console a few items - ( window.width() || variables || show how many elements of one kind in the document</li>
            <li>use it in a js file to get some console returns</li>
            <li>talk about why to use console - at any point in your logic to check and make sure it works i.e. console.log(‘this works’) - can be results as well</li>
          </ul>
        </li>
        <li id="Week10Step4">
          Discuss how to write to the screen - <code>document.write()</code>
          <ul>
            <li>write one of the elements you created in the previous step to the document</li>
            <li>talk about the difference between using console and writing to the document</li>
          </ul>
        </li>
        <li id="Week10Step5">
          Review important JavaScript terms - why they are important and when to use them
          <ul>
            <li>variables - strings | numbers | booleans</li>
            <li>arrays</li>
            <li>functions</li>
            <li>if / else</li>
            <li>for loops</li>
            <li>while loops</li>
          </ul>
        </li>
        <li id="Week10Step6">Show students <a href="http://jshint.com/" target="_blank">jshint</a> - drop the codes you created into jshint to discover what has been created - and any mistakes that need to be fixed</li>
        <li id="Week10Step7">Complete <a href="http://codifyacademy.com/digitalclock/" target="_blank">digital clock project</a> - the whole point is to get the Students to think logically - using the white board and prodding them for the answers to the logic behind a clock</li>
        <li id="Week10Step8">30 mins before the end of Sessions - introduce <a href="https://assets.adobe.com/" target="_blank">Adobe Assets</a> - how to load up PSDs - get CSS properties, height and width, offsets and layers from this program. - It is free to use online. - a good place to find free PSDs is <a href="http://www.freepik.com/" target="_blank">freepik.com</a> - you just have to attribute the created of the PSD</li>
      </ol>

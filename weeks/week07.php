    <!-- Step By Step for Class Time -->
    <div class="classTime">
      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week7Step1">Chit chat - have them show their neighbor the theme that they’ve been building and the neighbor provides constructive feedback on their work. 5-6 minutes total</li>
        <li id="Week7Step2">
          Start by telling students ways they can implement jQuery to add functionality to their projects. Show them how to use basic jQuery functions by walking them through <a href="http://www.w3schools.com/jquery/jquery_examples.asp" target="_blank">w3schools jQuery</a> examples. <em>1.5 hours max</em> - <a href="jQueryExamples.zip" class="linkage">Download jQuery Examples Zip</a> || view in Browser Version <a href="http://codifyacademy.com/jqueryExamples/" target="_blank" class="linkage">jQuery Examples</a>
          <ul>
            <li>how to include jQuery - <a href="http://jquery.com/download/" target="_blank">direct download</a> / <a href="http://jquery.com/download/" target="_blank">jQuery cdn</a> / <a href="https://developers.google.com/speed/libraries/devguide" target="_blank">google cdn</a></li>
            <li>how to use element selectors, i.e. elements, classes &amp; id’s</li>
            <li>events: click/hover/mouseenter/mouseleave</li>
            <li>hide/show/toggle, fade In/Out/Toggle, slide Up/Down/Toggle</li>
            <li>addClass, removeClass &amp; toggleClass</li>
          </ul>
        </li>
        <li id="Week7Step3">
          Show off jquery plugins -  introduce <a href="http://jqueryrain.com">jqueryrain.com</a>
          <ul>
            <li>talk about how to include plugin scripts - at the bottom and above your own main.js script</li>
            <li>show off the <a href="http://www.jqueryrain.com/?EpNv63PX">Facebook Photo Stream Plugin</a> - Examples { Grid | Collage | Circle } - <a href="http://codifyacademy.com/week7/fbPhotoStream/" class="linkage">Codify Plugin Example Code</a> &amp; <a href="https://github.com/chrisbrody/Facebook-WIT-Photo-Stream" class="linkage">Github Code</a></li>
            <li>show them how to download the 2 required files <a href="http://jquery.com/">jquery</a> &amp; <a href="http://www.gerardolarios.com/plugins-and-tools/fb-photo-stream/js/fb-photo-stream.min.js">photoStream.min.js</a></li>
            <li>have students create a new folder - { index.html | main.css | main.js } and add copies of the 2 files we need - create scripts and links for all files</li>
            <li>find an album to link to, if they need you can use our Women in Tech album <code>'449475298558996'</code> - continue to follow <strong>How to Use</strong>, <strong>Configuration</strong> and <strong>Options</strong></li>
            <li>create at least on steam with 5 images and a min count of 10, use CSS to hide any images more than 5</li>
            <li>add in the extra jQuery functionality for click events to hide and display more</li>
            <li>use google to search ‘best jquery plugin 2015’ allow students 5 - 10 mins to look for other cool plugins</li>
          </ul>
        </li>
        <li id="Week7Step4">Start planning what type of 1 page portfolio they want to build, photographer, videographer, chef, plumber, etc. Start searching for best portfolio in 2015 to get inspiration and ideas of what they can/should include</li>
        <li id="Week7Step5">Discuss what can be included in a portfolio website, have the Students list them out loud and you write it on the board. Example: about me section, contact form, gallery, services, clients, etc.</li>
        <li id="Week7Step6">Have Students write down what functionality they want to include on the sites they’re going to build today</li>
        <li id="Week7Step7">Students should then paper prototype what it will look like</li>
        <li id="Week7Step8">Start coding the 1 page portfolio site</li>
      </ol>

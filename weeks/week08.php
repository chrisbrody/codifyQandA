    <div class="classTime">
      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week8Step1">Chit chat - and answer any questions on jQuery</li>
        <li id="Week8Step2">Download and review <a href="http://www.initializr.com/" target="_blank">Bootstrap Boilerplate</a>, discuss line by line what each tag is doing...don’t worry about the content inside the body tag...except the scripts and the bottom</li>
        <li id="Week8Step3">Review the <a href="http://demosthenes.info/blog/777/Create-Fullscreen-HTML5-Page-Background-Video" target="_blank">background video</a> blog and have students recreate it in a copy of boilerplate, using the walkthrough</li>
        <li id="Week8Step4">Have students google “best front-end developer portfolios” to get inspiration for what their website could look like</li>
        <li id="Week8Step5">Discuss what can be included in a developers portfolio website, have the Students list them out loud and you write it on the board. Example: about me section, contact form, projects, etc.</li>
        <li id="Week8Step6">Have them write down (on a piece of paper) what they want to include on their portfolio sites</li>
        <li id="Week8Step7">Students should then paper prototype portfolio layouts</li>
        <li id="Week8Step8">Students start coding their 1 page portfolio sites</li>
        <li id="Week8Step9">With 1 hour left, Students should choose 1 plugin and add it to this week’s portfolio project</li>
        <li id="Week8Step10">Tell the students we will be buying them a domain name and hosting it for them for 1 year - have them brainstorm ideas to bring in next week for personal unique domain names. great examples for names - ralphcreates, frontendmatt, luigisfortress</li>
      </ol>

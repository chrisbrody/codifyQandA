      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week10Step1">Chit chat - and answer any questions on jQuery</li>
        <li id="Week10Step2">
          Review key coding concepts
          <ul>
            <li>How to include jQuery - direct download / jQuery cdn /google cdn</li>
            <li>document.ready v document.load</li>
            <li>how to use compound element selectors so you remain DRY</li>
            <li>events: mouseenter/mouseleave/click/dlbclick/hover</li>
            <li>fade In/Out/Toggle, slide In/Out/Toggle, show/hide</li>
            <li>css() v .animate()</li>
            <li>addClass and removeClass</li>
            <li>append/prepend vs after/before</li>
            <li>.val() - key for getting input data!</li>
          </ul>
        </li>
        <li id="Week10Step3">
          Have each student complete the tasks on the Using jQuery handout they are as follows:
          <ul>
            <li>hide() and show() an element on any event</li>
            <li>on hover popup using jQuery (like a tooltip, check out jQuery UI for this)</li>
            <li>on click popup on an element</li>
            <li>on scroll popup on an element</li>
            <li>add and remove a class on an element</li>
          </ul>
        </li>
        <li id="Week10Step4">Give students 5 to 10 minutes to show off app projects, encourage each team to discuss how they came up to the point they are in the project (what they did to come up with the idea, how the process was for paper prototyping, how the worked together coding and so on) - encourage students to give feedback and constructive criticism</li>
        <li id="Week10Step5">
          Discuss what needs to be the main focus from here on out
          <ul>
            <li>linkedin &amp; networking - have them all connect on linkedin if they havenâ€™t yet</li>
            <li>portfolio</li>
            <li>portfolio projects</li>
          </ul>
        </li>
        <li id="Week10Step6">
          Have students <b>brainstorm</b> ideas for their portfolios &amp; projects
          <ul>
            <li>portfolios should showcase who you are as a person not just that you program now</li>
            <li>projects can range from, but not limited to: simple website update, psd conversion to html, theme replication, theme creation, web apps</li>
          </ul>
        </li>
        <li id="Week10Step7">Show how <a href="https://assets.adobe.com" target="_blank">Adobe.Assets</a> can be used to convert PSDs - go through the signup process ( no need to download right now ) - load a PSD and inspect to see dimensions &amp; css as well as layers for retrieving images!</li>
        <li id="Week10Step8">Find a PSD and start the paper prototype process w/content - begin with large with sheets of paper and some pens</li>
        <li id="Week10Step9">Find a theme &amp; plugins for the selected PSD - time to troll the internet for some things that have already been created that we can modify to fit our project perfectly!</li>
        <li id="Week10Step10">If time remains, students can begin coding their chosen PSD</li>

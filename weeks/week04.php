    <!-- Step By Step for Class Time -->
    <div class="classTime">
      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week4Step1">
        Time: 5-10 mins - Chit chat
        <ul>
          <li>ask about Students experiences so far - they have learned a lot about html &amp; css</li>
          <li>remind Students that it is a process and the best way to learn is by building projects</li>
          <li>remind Students to always paper prototype before coding</li>
        </ul>
        </li>
        <li id="Week4Step2">
          Introduce students to and get them using <a href="http://daneden.github.io/animate.css/" target="_blank">CSS Animations</a> - talk about how and why it can be useful - then include it into google project
          <ul>
            <li>include link into head of document - <code>&lt;link rel="stylesheet" href="assets/css/animate.min.css"&gt;</code></li>
            <li>download raw <a href="https://raw.githubusercontent.com/daneden/animate.css/master/animate.min.css" target="_blank">animate.min.css</a> and save to css folder in your project</li>
            <li>have students reference <a href="https://github.com/daneden/animate.css" target="_blank">animate.css on github</a> for usage</li>
            <li>have everyone create an example in their google project</li>
          </ul>
        </li>
        <li id="Week4Step3">
          Talk about how many users are on mobile devices
          <ul>
            <li>70% of users and growing  so making them responsive is very important. This can be achieved using media queries</li>
            <li>basic ex. Walk them through and have them test out the media query example on w3schools.com. Be sure to change the size and add css rules. <a href="http://www.w3schools.com/cssref/css3_pr_mediaquery.asp" target="_blank">w3schools media queries</a></li>
          </ul>
        </li>
        <li id="Week4Step4">
          Download the resizing tool available in the chrome and firefox dev-tools
          <ul>
            <li>this allows us to resize the browser to specific viewport sizes</li>
            <li>check out <a href="http://spirelightmedia.com/resources/responsive-design-device-resolution-reference" target="_blank">common viewport sizes</a> - explain that you should use media queries for common viewports</li>
          </ul>
        </li>
        <li id="Week4Step5">
          Begin making the Google Replication project responsive using media queries
          <ul>
            <li>start by having everyone reduce the size of their browser window</li>
            <li>begin with the navigation, if needed</li>
            <li>then the middle section</li>
            <li>finally the bottom navigation</li>
          </ul>
        </li>
        <li id="Week4Step6">If extra time remains have students add media queries to either 37signals or their PSD replication - mention that these need to be finished this week</li>
        <li id="Week4Step7">
          15 mins before the end of class
          <ul>
            <li>Show Students how to include <a href="http://getbootstrap.com/getting-started/#download" target="_blank">Bootstrap CSS CDN</a></li>
            <li>talk about <a href="http://codifyacademy.com/bootstrap3/" target="_blank">Bootstrap 3</a>  (mainly how it is a library of CSS class we call to get a desired effect) - draw examples on the whiteboard</li>
          </ul>
        </li>
      </ol>

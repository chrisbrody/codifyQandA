    <!-- Step By Step for Class Time -->
    <div class="classTime">
      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week9Step1">Chit chat - talk about the importance of using a theme - when it can be useful or counterproductive to certain situations</li>
        <li id="Week9Step2">Review portfolios - give each student 3-5 mins to showcase their portfolio and have other students and yourself constructive criticism</li>
        <li id="Week9Step3">
          Check out <a href="http://bootstraphero.com/the-big-badass-list-of-twitter-bootstrap-resources" target="_blank">bootstraphero</a> for 300 + resources for bootstrap - ready made code - pick your top 5 to review and talk about how they are useful in everyday coding or on a project you worked on
        </li>
        <li id="Week9Step4">
          Review how to add <a href="http://getbootstrap.com/javascript/" target="_blank">bootstrap js</a> functionality into an elements, also now is time to discuss what versions we should be including when projects start vs when a project is completed
          
        </li>
        <li id="Week9Step5">
          Show how to <a href="http://getbootstrap.com/customize/" target="_blank">customize bootstrap</a>
          
        </li>
        <li id="Week9Step6">Discuss how to add additional pages and link to them - this will have to be a 2 step process - first show them how to do it on the whiteboard and then have each student do it in browser - easiest way would be to use a bootstrap example (navbar)</li>
        <li id="Week9Step7">This class, students will be creating a multi-page website, 3-5 pages, which will be one of their portfolio projects. - Demonstrate how to link pages together, have the students replicate it - keep it simple</li>
        <li id="Week9Step8">First, they need to decide what type of website to code. Ideally, something that interests them or something that will help them find a job. - Using <a href="https://wrapbootstrap.com/" target="_blank">Wrapbootstrap</a> check out some of the top selling themes and have students draw inspiration from these. - to begin the process of creating their own theme from scratch - explain this is a two week process - this week will be used to complete the paper prototype</li>
        <li id="Week9Step9">Begin paper prototyping out desired layouts</li>
        <li id="Week9Step10">15 mins before the end of class - talk about choosing a domain name ( student name vs clever name ) - challenge everyone to bring in a few names next week &amp; their completed paper prototype - will be sharing these next week</li>
        <li id="Week9Step11">Hand out JavaScript programming books - <b>Explain:</b> They’ll do 3 chapters a week for 3 weeks, they can finish the rest of the book in their free time. Learning JavaScript is not about being able to code JavaScript from scratch. It’s about learning JavaScript’s syntax (grammar), so you’ll be able to quickly learn JavaScript libraries (like jQuery) and frameworks (like Angular.jS).</li>
      </ol>

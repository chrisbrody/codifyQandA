    <!-- Step By Step for Class Time -->
    <div class="classTime">
      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week3Step1">
          Chit chat - 10 - 15 mins
          <ul>
            <li>about what they found to be the most interesting thing they came across in coding this week</li>
            <li>answer any questions about HTML &amp; CSS</li>
            <li>
              draw on the board &amp; discuss proper terminology for CSS selectors, properties, values
              <div class="well">
                <code>selector( body {
                  <code>property( color: )</code><code>value ( red;)</code>
                })</code>
              </div>
            </li>
          </ul>
        </li>
        <li id="Week3Step2">
          Review how to center divs and images
          <ul>
            <li>using <code>margin: 0 auto; display:block;</code> and providing the div with a width. Remember that the parent element must have a defined width in order for it to calculate the margin</li>
            <li>alternatively you can use <code>text-align: center;</code> on the parent div to center anything inside</li>
          </ul>
        </li>
        <li id="Week3Step3">
          Download colorpick eyedropper and page ruler in chrome extension, download colorzilla and measurelt in the add-ons if you have firefox
          <ul>
            <li>colorpick / colorzilla are great tools for finding out the color of any element</li>
            <li>page ruler / measureIt are great for measuring any element to determine the width and height of an element</li>
          </ul>
        </li>
        <li id="Week3Step4">
          Use the new extensions for 3 - 5 mins on any website - to get acquainted with them
          <ul>
            <li>have students draw out any website element and determine what tags would need to be used to create the element</li>
            <li>if they need have them google simple ui elements - to find one</li>
          </ul>
        </li>
        <li id="Week3Step5">
          Take some time to introduce students to <a href="https://www.canva.com/" target="_blank">Canva</a>
          <ul>
            <li>have everyone create a user login</li>
            <li>show some of our canva stuff, and talk about how you went about creating your canva projects</li>
            <li>walkthrough how to create canva projects - searching elements - loading pictures ect. - can be used to create elements for any project</li>
          </ul>
        </li>        
        <li id="Week3Step6">
          Discuss the importance of PSD’s in the industry
          <ul>
            <li>most companies hire a designer and developer to work together. The designer will design a page and send the photoshop files to the developer. When that happens, you won’t have access to source code or be able to inspect the css in the dev tools</li>
            <li>its good to get good at coding from scratch, to help you find a job easier</li>
          </ul>
        </li>
        <li id="Week3Step7">
          Time to select a PSD to replicate this week
          <ul>
            <li>in google go to the image tab, type in “photoshop website”, click on the images tab, and choose of of these for your PSD replica projects</li>
            <li>choose something that’s challenging, but not unlikely for you to finish</li>
          </ul>
        </li>
        <li id="Week3Step8">
          paper prototype out the psd that they’re going to be replicating ( remind everyone of the importance of this step )
          <ul>
            <li>your draw out what you see ( or want to create )</li>
            <li>once the layout is complete - define tags you want to use and any css properties that can be applied to replicate</li>
            <li>its important to define what you want to create before you start coding</li>
          </ul>
        </li>
        <li id="Week3Step9">Once the paper prototype is done have the students begin coding from scratch</li>
      </ol>

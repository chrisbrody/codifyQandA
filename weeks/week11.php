    <div class="classTime">
      <h3>Step By Step Power Coding Session Process</h3>
      <ol>
        <li id="Week11Step1">Chit chat - and play <a href="http://multeor.com/about/" target="_blank">Multeor</a> - if you play 3 games - 2 groups play a round, take the top 2 high scorers from each group and play a final match with top 4 players</li>
        <li id="Week11Step2">Take 5 - 10 mins for each student to present their finished PSD to their neighbor - offer praise and constructive feedback for the project</li>
        <li id="Week11Step3">
            Review JS topics covered this week and the past - when to logically use them
            <ul>
              <li>for loops</li>
              <li>while loops</li>
              <li>objects</li>
              <li>case / switch</li>
            </ul>
        </li>
        <li id="Week11Step4">Create <a href="http://codifyacademy.com/project_calculator/" target="_blank">Simple JS Calculator</a> - using this walk through - make sure to get students to logically think how a calculator works before writing any code</li>
        <li id="Week11Step5">Select teams for app/website projects - use your discretion to select the teams - ideally 2 students per team</li>
        <li id="Week11Step6">Begin brainstorming ideas for project - allow the groups to split off for 30 - 45 mins paper prototyping</li>
        <li id="Week11Step7">When the ready go to each group and critique the  paper prototyping - give each group 5 mins to express their idea for their new creation / present &amp; critique paper prototypes</li>
        <li id="Week11Step8">15 mins before the end of class - Have each group decide who will do what for the project during the week - will complete this during week 12</li>
      </ol>
